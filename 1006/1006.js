var str = "5.0 10.0 10.0";
var lines = str.split(" ");

var peso1 = 0.2, peso2 = 0.3, peso3 = 0.5;

var a = parseFloat(lines.shift()) * (peso1);
var b = parseFloat(lines.shift()) * (peso2);
var c = parseFloat(lines.shift()) * (peso3);

console.log("MEDIA = "+parseFloat(a+b+c).toFixed(1));